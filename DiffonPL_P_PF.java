class DiffonPL_P_PF {

    public static void main(String[] args) {
        //Effects of println
        System.out.println("Rojesh");
        System.out.println("Shrestha");
        System.out.println();

        //Effects of print
        System.out.print("Rojesh ");
        System.out.print("Shrestha");
        System.out.println();
        System.out.println();

        //Effects of printf
        System.out.printf("Rojesh ").printf("Shrestha.");

    }
}