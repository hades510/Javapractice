package operators.Assignment_operators;

public class Arithematic_assign {
    public static void main(String[] args) {
        float num = 10f;
        num +=20f;
        System.out.println(num);

        num -=20f;
        System.out.println(num);

        num *=20f;
        System.out.println(num);

        num /=10f;
        System.out.println(num);

        num %=11f;
        System.out.println(num);
    }
}
